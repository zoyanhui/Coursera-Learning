x1 = [1, 0]; x2 = [0, 1]; x3 = [0, -1]; x4 = [-1, 0]; x5 = [0, 2]; x6 = [0, -2]; x7 = [-2 ,0];
N = 7;
X = [x1; x2; x3; x4; x5; x6; x7];
Y = [-1;-1;-1;1;1;1;1];

P = [-1, -1, -1, -1, -1, -1, -1]';
LB = [0, 0 ,0, 0, 0, 0, 0];
Q = (Y * Y') .* ((1 + X * X').^2);
A = [Y, -Y]';
C = [0, 0]';
[ALPHA, OBJ, INFO, LAMBDA] = qp([], Q, P, A, C, LB, []);

Z = [ones(7,1) sqrt(2)* X(:,1) sqrt(2)* X(:,2) X(:,1).^2 X(:,1).*X(:,2) X(:,2).*X(:,1) X(:,2).^2];

% 原始恐惧超平面，系数：
sum(ALPHA .* Y .* Z) .* [1, sqrt(2), sqrt(2), 1, 1, 1, 1]

